package kumar;

public class Varable {
 int a=10;  // instant varaible
 int c =30;
 float  f = 251.455555f;
 float f1 = 34.11111f;
 float f2= 1111.444444f;
 float f3= 111111.12344f;
 double d= 3.2555555;
 
 static int b=20;
 static int b1=20;
 static int b2=20;
 static int b3=20;
 
		 
 
 
 
 
	public static void main(String[] args) {
		
		
		 Varable var = new Varable();  // instant variable
		System.out.println(var .a);
		System.out.println(var .c);
		System.out.println(var .f);
		System.out.println(var .f1);
		System.out.println(var .f2);
		System.out.println(var .f3);
		System.out.println(var .d);
		
		
		
		
		
		System.out.println(b);// static varaible
		System.out.println(b1);
		System.out.println(b2); 
		System.out.println(b3); 
		
		
	}

}
